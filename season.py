n = raw_input("Enter Season:")
if n == "November" or n == "December" or n == "January" or n == "February" or n == "March":
	print ("Winter")
elif n == "April" or n == "May":
	print ("Spring") 
elif n == "June" or n == "July" or n == "August":
	print ("Summer")
elif n == "September" or n == "October":
	print ("Autumn")
